location = ['Hawaii','Maldives','Africa','Japan']


print("Original: ",location)
print("Sorted: ",sorted(location))

print("\nOriginal: ",location)
print("Reverse Sorted: ",sorted(location, reverse=True))

print("\nOriginal: ",location)
location.reverse()
print("Reversed: ",location)

print("\nReversed: ",location)
location.reverse()
print("Back to Original: ",location)

print("\nOriginal: ",location)
location.sort()
print("Sorted: ",location)

print("\nSorted: ",location)
print("Reverse Sorted: ",sorted(location, reverse=True))